        <h2>Sobre a Startup</h2>
        <p>
            A <strong>Startup Segurança Alimentar</strong> surgiu em um contexto de desafios crescentes no Brasil e no mundo. Após a pandemia, a insegurança alimentar se agravou, afetando milhões de pessoas. Relatórios da imprensa, como os do <a href="https://oglobo.globo.com/brasil/noticia/2023/07/12/mapa-da-fome-insegurancaalimentar-se-agravou-na-pandemia-e-atingiu-mais-de-70-milhoes-debrasileiros.ghtml" target="_blank">O Globo</a> e da <a href="https://www1.folha.uol.com.br/folha-social-mais/2023/09/oito-graficos-que-explicam-a-escalada-da-fome-no-brasil.shtml" target="_blank">Folha de São Paulo</a>, indicam que a fome e a insegurança alimentar atingiram mais de 70 milhões de brasileiros.
        </p>
        <p>
            Nossa startup foi criada com o propósito de combater essa insegurança alimentar, utilizando inovação tecnológica e uma abordagem sustentável. Nós desenvolvemos uma <strong>fazenda urbana</strong>, onde aplicamos inteligência artificial para maximizar a produção de alimentos de forma eficiente e sustentável. 
        </p>
        <p>
            Nossa solução é baseada em uma API de inteligência artificial gratuita, desenvolvida por uma universidade fictícia, que oferece recomendações sobre quais alimentos plantar em diferentes épocas do ano, levando em consideração a localidade geográfica. 
        </p>
        <p>
            Com base nos <a href="https://brasil.un.org/pt-br/sdgs" target="_blank">Objetivos de Desenvolvimento Sustentável (ODS)</a> da ONU, seguimos rigorosamente as práticas ESG (Environmental, Social, Governance), um conjunto de políticas focadas em sustentabilidade ambiental, social e de governança. Entendemos que é fundamental alinhar nossos esforços de inovação tecnológica com um impacto positivo na sociedade e no meio ambiente.
        </p>
        <p>
            Um dos nossos principais objetivos é apresentar a viabilidade de nossa fazenda urbana para a segurança alimentar na <strong>COP 30</strong> — a 30ª Conferência das Partes da Convenção-Quadro das Nações Unidas sobre Mudança do Clima. O evento será realizado em Belém, no Pará, entre os dias 10 e 21 de novembro de 2025, e estamos determinados a demonstrar como a tecnologia pode transformar a produção de alimentos em centros urbanos.
        </p>

        <p>Saiba mais sobre ESG <a href="https://conectabrasil.org/#/blogs/details/a-importancia-do-esg" target="_blank">aqui</a>.</p>
